def _clean_env_secret(name: str, default='') -> str:
    raw = os.environ.get(name)
    if raw is None:
        return default
    s = str(raw).strip()
    if len(s) >= 2 and s[0] == s[-1] and s[0] in "'\"":
        s = s[1:-1].strip()
    return s or default


